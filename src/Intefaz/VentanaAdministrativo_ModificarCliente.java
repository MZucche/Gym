    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String tipo = jComboBox1.getSelectedItem().toString(); // muestra selecci贸n
        if (tipo.equals("BLACK")) { // selecciono opcion TipoSocio correcta
            tipoSocio = TipoSocio.BLACK;
        } else if (tipo.equals("ORO")) {
            tipoSocio = TipoSocio.ORO;
        } else {
            tipoSocio = TipoSocio.PLATINUM;
        }
        
        Supertlon.cambiarTipoSocioDelSocio(socioSeleccionado, tipoSocio);
        JOptionPane.showMessageDialog(null, "Se modific贸 el TIPO SOCIO correctamente!");
    }//GEN-LAST:event_jButton3ActionPerformed

