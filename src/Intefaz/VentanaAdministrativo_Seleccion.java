    public VentanaAdministrativo_Seleccion(Supertlon controller) {
        initComponents();
        this.controller = controller;
        
        
        List<Administrativo> Administrativos = Supertlon.mostrarAdministrativos();
        DefaultComboBoxModel<String> opciones1 = new DefaultComboBoxModel<>();
        // Llenar el modelo con representaciones de los objetos Articulo
        if (Administrativos.isEmpty()) {
                jComboBox1.addItem("NO EXISTEN ADMINISTRATIVOS");
        } else {
            for (Administrativo administrativo : Administrativos) {
                String id = String.valueOf(administrativo.getId());
                opciones1.addElement(id);
            }
            jComboBox1.setModel(opciones1); 
        }
    }

