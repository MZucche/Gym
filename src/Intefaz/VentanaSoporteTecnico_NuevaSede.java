    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String tipoSocio = jComboBox1.getSelectedItem().toString(); // muestra selección
        String localidad = jComboBox2.getSelectedItem().toString(); // muestra selección
       
        controller.eliminarLocalidad(localidad); // elimino localidad se supertlon
        
        if (tipoSocio.equals("BLACK")) { // selecciono opcion TipoSocio correcta
            socio = TipoSocio.BLACK;
        } else if (tipoSocio.equals("ORO")) {
            socio = TipoSocio.ORO;
        } else {
            socio = TipoSocio.PLATINUM;
        }
        
        if (jComboBox2.getSelectedItem().equals("NO EXISTEN LOCALIDADES")) { 
            JOptionPane.showMessageDialog(null, "Debe existir una localidad. No se puede crear la Sede.");
        } else {
            if (jComboBox2.getSelectedItem().equals("NO EXISTEN LOCALIDADES") || listaArticulos.isEmpty()) {
                JOptionPane.showMessageDialog(null, "AVISO: La Sede se cargó sin Articulos.");
            }
            int id = controller.crearSede(socio, localidad, listaArticulos); // crear nueva SEDE
            if (jToggleButton1.isSelected()) {
                Sede sede = Supertlon.buscarSede(id);
                for (CargaEmplazamiento cargaEmp : listaEmplazamiento) {
                    controller.crearEmplazaminto(cargaEmp.cargaCapacidad, sede, cargaEmp.cargaTipo);
                }
                JOptionPane.showMessageDialog(null, "EMPLAZAMIENTO creado con éxito!");
            }
            jComboBox2.removeItemAt(jComboBox2.getSelectedIndex()); // saca item seleccionado
            listaArticulos = new ArrayList<>();
            JOptionPane.showMessageDialog(null, "SEDE creada con éxito!");
        }       
 
        if (jComboBox2.getItemCount() == 0) {
            jComboBox2.addItem("NO EXISTEN LOCALIDADES");
        }
        System.out.println(jComboBox2.getItemCount()); // BORRAR
        
    }//GEN-LAST:event_jButton1ActionPerformed

