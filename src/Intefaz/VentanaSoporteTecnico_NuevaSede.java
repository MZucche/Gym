    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String tipoEmplazamiento = jComboBox4.getSelectedItem().toString();
        String capacidad = jComboBox5.getSelectedItem().toString();
        int cap = Integer.parseInt(capacidad);
                
        if (tipoEmplazamiento.equals("Pileta")) {
            emplazamiento = TipoEmplazamiento.PILETA;
        } else if (tipoEmplazamiento.equals("Aire Libre")) {
            emplazamiento = TipoEmplazamiento.AIRE_LIBRE;
        } else {
            emplazamiento = TipoEmplazamiento.SALON;
        }
        CargaEmplazamiento cargaEmplazamiento = new CargaEmplazamiento();
        cargaEmplazamiento.cargaCapacidad = cap;
        cargaEmplazamiento.cargaTipo = emplazamiento;
        listaEmplazamiento.add(cargaEmplazamiento);
        JOptionPane.showMessageDialog(null, "Se agregó emplazamiento!");
        
    }//GEN-LAST:event_jButton3ActionPerformed


