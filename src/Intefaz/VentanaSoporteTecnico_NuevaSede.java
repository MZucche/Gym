    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int num = jComboBox3.getSelectedIndex();
        if (jComboBox3.getSelectedItem().equals("NO EXISTEN ARTICULOS")) {
            JOptionPane.showMessageDialog(null, "No existen articulos.");
        } else if (!(jComboBox2.getSelectedItem().equals("NO EXISTEN LOCALIDADES"))) { // si la localidad no es NO EXISTEN LOCALIDADES, puede seleccionarse articulos
            
            String seleccionado = jComboBox3.getSelectedItem().toString(); // agarro la opcion seleccionada
            String[] partes = seleccionado.split("\\ - "); // separo string de JComboBox para tener ID
            int id = Integer.parseInt(partes[0]); // guardo el ID para buscarlo en la lista de Articulos
            Articulo articuloSeleccionado = Supertlon.buscarArticulo(id); // selecciono articulo con todos sus atributos
            this.listaArticulos.add(articuloSeleccionado); // agrego en lista articulos
            Supertlon.eliminarArticulo(articuloSeleccionado); // elimino de supertlon 
            jComboBox3.removeItemAt(num); // elimino de interfaz
            
        } else {
            JOptionPane.showMessageDialog(null, "Debe existir una localidad para cargar Articulos");
        }
 
        if (jComboBox3.getItemCount() == 0) {
            jComboBox3.addItem("NO EXISTEN ARTICULOS");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

